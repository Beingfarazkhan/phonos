import { Hand, ArrowLeftIcon } from "lucide-react"
import { formatDistanceToNow } from 'date-fns'
import { WidgetHeader } from "../components/widget-header"
import { useAtomValue, useSetAtom } from "jotai"
import { contactSessionIdAtomFamily, conversationIdAtom, organizationIdAtom, screenAtom } from "../../atoms/widget-atom"
import { WidgetFooter } from "../components/widget-footer"
import { Button } from "@workspace/ui/components/button"
import { ConversationStatusIcon } from "@workspace/ui/components/conversation-status-icon"
import { usePaginatedQuery } from "convex/react"
import { api } from "@workspace/backend/_generated/api"
import { useInfiniteScroll } from "@workspace/ui/hooks/use-infinite-scroll"
import { InfiniteScrollTrigger } from "@workspace/ui/components/infinite-scroll-trigger"


export const WidgetInboxScreen = () => {
    const setScreen = useSetAtom(screenAtom)
    const setConversationId = useSetAtom(conversationIdAtom)

    const organizationId = useAtomValue(organizationIdAtom)
    const contactSessionId = useAtomValue(contactSessionIdAtomFamily(organizationId || ""))

    const conversations = usePaginatedQuery(
        api.public.conversations.getMany,
        contactSessionId ? {
            contactSessionId
        } : "skip",

        {
            initialNumItems: 10
        }
    )

    const { topElementRef, handleLoadMore, canLoadMore, isLoadingMore } = useInfiniteScroll({
        status: conversations.status,
        loadMore: conversations.loadMore,
        loadSize: 10
    })

    return (
        <>
            <WidgetHeader className="rounded-b-2xl">
                <div className="flex items-center gap-x-2 p-3 font-semibold text-secondary">
                    <Button
                        size={"icon"}
                        onClick={() => setScreen("selection")}
                        variant={"transparent"}
                    >
                        <ArrowLeftIcon />
                    </Button>
                    <p>Inbox</p>
                </div>
            </WidgetHeader>
            <div className="flex flex-1 flex-col overflow-y-auto gap-y-2 p-5 ">
                {conversations?.results.length > 0 &&
                    conversations?.results.map((conversation) => (
                        <Button
                            key={conversation._id}
                            className="h-20 w-full justify-between"
                            onClick={() => {
                                setConversationId(conversation._id)
                                setScreen("chat")
                            }}
                            variant={"outline"}
                        >
                            <div className="flex flex-col w-full gap-4 overflow-hidden text-start">
                                <div className="flex w-full items-center justify-between gap-x-2">
                                    <p className="text-muted-foreground text-xs">Chat</p>
                                    <p className="text-muted-foreground text-xs">{formatDistanceToNow(new Date(conversation._creationTime))}</p>
                                </div>
                                <div className="flex w-full items-center justify-between gap-x-2">
                                    <p className="truncate text-sm">{conversation.lastMessage?.text}</p>
                                    <ConversationStatusIcon status={conversation.status} />
                                </div>
                            </div>
                        </Button>
                    ))
                }
                <InfiniteScrollTrigger
                    noMoreText="No more items."
                    ref={topElementRef}
                    onLoadMore={handleLoadMore}
                    canLoadMore={canLoadMore}
                    isLoadingMore={isLoadingMore}
                />
            </div>
            {/* <WidgetFooter /> */}
        </>
    )
}